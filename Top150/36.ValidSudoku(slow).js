/**
 * @param {character[][]} board
 * @return {boolean}
 */
var isValidSudoku = function(board) {
  const rows = {};
  const cols = {};
  const boxs = {};

  for (let i = 0; i < 9; i++) {
    for (let j = 0; j < 9; j++) {
      if (rows[i] && rows[i][board[i][j]]) return false;
      if (cols[j] && cols[j][board[i][j]]) return false;
      if (
        boxs[`${(i / 3) | 0}-${(j / 3) | 0}`] &&
        boxs[`${(i / 3) | 0}-${(j / 3) | 0}`][board[i][j]]
      )
        return false;
      if (board[i][j] !== ".") {
        rows[i] = {
          ...rows[i],
          [board[i][j]]: true
        };
        cols[j] = {
          ...cols[j],
          [board[i][j]]: true
        };
        boxs[`${(i / 3) | 0}-${(j / 3) | 0}`] = {
          ...boxs[`${(i / 3) | 0}-${(j / 3) | 0}`],
          [board[i][j]]: true
        };
      }
    }
  }

  return true;
};



